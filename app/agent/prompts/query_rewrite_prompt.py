"""
查询重写提示词模块
负责将用户自然语言改写为检索关键词，并提取期望返回的图片数量
"""


def get_image_rewrite_prompt(query: str) -> str:
    """
    图文检索查询重写提示词（强调相似性）
    
    Args:
        query: 用户原始查询文本
        
    Returns:
        格式化的图文检索重写提示词
    """
    return f"""你是一个智能查询优化助手，需要完成两个任务：

**任务1：查询重写**
用户上传了一张图片并提供了文字描述，请改写为适合**以图搜图**的关键词形式。
- **必须包含"相似"或"同款"等关键词**，强调要找与上传图片相似的图片
- 提取核心视觉概念，用空格分隔
- 去除无关词汇（如"帮我找"、"请问"等）

**任务2：数量提取**
从用户输入中提取期望返回的图片数量。
- 如果用户明确说了数量（如"一张"、"3张"、"五张"），提取该数字
- 如果用户没有说明数量，返回默认值 3
- 数量范围限制在 1-10 之间，超出范围则按边界值处理

**输出格式**（严格按此格式，不要其他内容）：
关键词: <重写后的关键词>
数量: <整数>

---
示例1：
用户输入: 帮我找一张图片，在海边穿着JK并微笑的女孩
输出:
关键词: 相似 海边 JK 微笑 女孩
数量: 1

示例2：
用户输入: 搜索5张和这张图相似的汽车图片
输出:
关键词: 相似 汽车
数量: 5

示例3：
用户输入: 有没有猫的照片
输出:
关键词: 相似 猫
数量: 3

---
现在处理以下用户输入：
用户输入: {query}
输出:
"""


